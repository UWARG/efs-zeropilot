void USART2_IRQHandler(void)
{
	/* UART IDLE Interrupt */
	if((USART2->ISR & USART_ISR_IDLE) != RESET)
	{
		USART2->ICR = UART_CLEAR_IDLEF;
		/* Start DMA timer */
		hdma_uart2_timer = UART2_DMA_TIMEOUT;
	}

	HAL_UART_IRQHandler(&huart2);
}


